/**
 * 检查字段是否为套话
 * @param {string} field - 字段名
 * @param {string} value - 字段值
 * @returns {boolean} 是否为套话
 */
function isBoilerplate(field, value) {
  if (!value) return false;
  const pattern = BOILERPLATE_PATTERNS[field];
  return pattern && pattern.test(value);
}

/**
 * 主函数
 */
function main() {
  console.log('开始分析思维模型...\n');
  
  // 读取文件
  const content = fs.readFileSync(MODELS_PATH, 'utf-8');
  const models = JSON.parse(content);
  
  console.log(`共 ${models.length} 个模型\n`);
  
  // 统计套话模型
  const boilerplateModels = [];
  models.forEach((model, index) => {
    const fields = ['deepAnalysis', 'principle', 'scope', 'tips', 'practice'];
    const boilerplateFields = fields.filter(f => isBoilerplate(f, model[f]));
    if (boilerplateFields.length > 0) {
      boilerplateModels.push({
        index: index + 1,
        name: model.name,
        fields: boilerplateFields
      });
    }
  });
  
  console.log(`发现 ${boilerplateModels.length} 个包含套话的模型\n`);
  
  // 显示所有需要优化的模型
  console.log('需要优化的模型列表：');
  boilerplateModels.forEach(m => {
    console.log(`${m.index}. ${m.name}`);
  });
}

main();
