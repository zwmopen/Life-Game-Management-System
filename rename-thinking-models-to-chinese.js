// 获取思维模型目录路径
const modelsDir = path.join(__dirname, 'thinking-models');

// 读取目录中的所有文件
fs.readdir(modelsDir, (err, files) => {
  if (err) {
    console.error('无法读取目录:', err);
    return;
  }

  // 过滤出所有的HTML文件
  const htmlFiles = files.filter(file => path.extname(file).toLowerCase() === '.html');

  // 遍历每个HTML文件
  htmlFiles.forEach(file => {
    const basename = path.basename(file, '.html'); // 获取不含扩展名的文件名
    
    // 检查是否在翻译映射中
    if (modelTranslations[basename]) {
      const newName = `${modelTranslations[basename]}.html`;
      const oldPath = path.join(modelsDir, file);
      const newPath = path.join(modelsDir, newName);

      // 检查新文件名是否已存在
      if (!fs.existsSync(newPath)) {
        console.log(`重命名: ${file} -> ${newName}`);
        fs.rename(oldPath, newPath, (err) => {
          if (err) {
            console.error(`重命名失败 ${file}:`, err);
          }
        });
      } else {
        console.log(`跳过 ${file}: 新文件名 ${newName} 已存在`);
      }
    } else {
      // 如果找不到对应的中文名称，则跳过
      console.log(`跳过: ${file} (无对应中文名称)`);
    }
  });
});