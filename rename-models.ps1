# 定义输出目录
$outputDir = "d:\AI编程\人生游戏管理系统\thinking-models"

# 创建重命名日志
$logFile = "$outputDir\rename_log.txt"
"重命名日志 - $(Get-Date)" | Out-File -FilePath $logFile -Encoding UTF8
"中文名称,英文名称,旧文件名,新文件名" | Add-Content -Path $logFile -Encoding UTF8

# 遍历所有HTML文件
Get-ChildItem -Path $outputDir -Filter "*.html" | ForEach-Object {
    $oldFileName = $_.Name
    $oldFilePath = $_.FullName
    
    # 跳过已经是中文在前英文在后格式的文件
    if ($oldFileName -match "^[^_]+_[^_]+\.html$") {
        "跳过已符合格式的文件: $oldFileName" | Add-Content -Path $logFile -Encoding UTF8
        return
    }
    
    # 查找对应的中文名称
    $matchedModel = $modelMappings.GetEnumerator() | Where-Object { $oldFileName -eq "$($_.Value).html" }
    
    if ($matchedModel) {
        $chineseName = $matchedModel.Key
        $englishName = $matchedModel.Value
        $newFileName = "${chineseName}_${englishName}.html"
        $newFilePath = Join-Path -Path $outputDir -ChildPath $newFileName
        
        # 重命名文件
        Rename-Item -Path $oldFilePath -NewName $newFileName -Force
        
        # 记录日志
        "$chineseName,$englishName,$oldFileName,$newFileName" | Add-Content -Path $logFile -Encoding UTF8
        Write-Host "重命名: $oldFileName -> $newFileName"
    } else {
        "未找到映射: $oldFileName" | Add-Content -Path $logFile -Encoding UTF8
        Write-Host "未找到映射: $oldFileName"
    }
}

Write-Host "重命名完成，日志文件: $logFile"